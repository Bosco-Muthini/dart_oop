void main(){

// Display even numbers between 50 - 100
  for(int i=50; i<=100; i++){
    if(i%2 == 0){
      print(i);
    }
  } 

//   While loop to print 1 - 10
  int i = 1;
  while (i <= 10) {
    print(i);
    i++;
  }


// Do while loop to print 1 - 10
  int d = 1;
  do {
    print(d);
    d++;
  } while (d <= 10);

}